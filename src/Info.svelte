<main>
	<h1>
		<strong
			>How much <span style="color:#ff3c38;">misinformation</span> are you
			exposed to?</strong
		>
	</h1>

	<p>
		<span class="bold">Misinformation exposure</span> scores measure how
		much the politicians and public organizations you follow tend to lie
		(based on fact-checking their claims by
		<a href="https://www.politifact.com/" target="_blank">PolitiFact</a>).
		These scores go from 0 (none of the fact-checked claims by politicians
		and public figures you follow are false) to 1 (all of the fact-checked
		claims by politicians and public organizations you follow are false).
	</p>

	<p>
		<span class="bold">Partisanship scores</span> measure how much you tend to
		follow politicians from the left versus right side of the political spectrum.
		These scores go from -1 (follow only left-leaning accounts) to 1 (follow
		only right-leaning accounts).
	</p>

	<p>
		You can also use our <a
			href="https://github.com/mmosleh/minfo-exposure"
			target="_blank">API</a
		>
		to get the scores. Source code for this app is available
		<a
			href="https://github.com/hauselin/misinfo-expose-svelte"
			target="_blank">here</a
		>.
	</p>
	<p>
		Citation: <strong
			><i
				><a
					href="https://www.nature.com/articles/s41467-022-34769-6"
					target="_blank"
					>Measuring exposure to misinformation from political elites
					on Twitter.</a
				></i
			></strong
		>
		Mosleh, M. & Rand, D.G., Nature Communications, 2022. See
		<a
			href="https://twitter.com/_mohsen_m/status/1482072249427505152"
			target="_blank">Tweet thread</a
		>.
	</p>
</main>

